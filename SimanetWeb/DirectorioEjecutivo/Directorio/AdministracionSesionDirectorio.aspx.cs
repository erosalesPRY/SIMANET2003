		public void LlenarGrillaOrdenamientoPaginacion(string columnaOrdenar,int indicePagina)
		{
			CSesionDirectorio oCSesionDirectorio =  new CSesionDirectorio();
			DataTable dtSesionDirectorio =  oCSesionDirectorio.ConsultarSesionesDirectorio();
			
			if(dtSesionDirectorio!=null)
			{
				DataView dwSesionDirectorio = dtSesionDirectorio.DefaultView;
				dwSesionDirectorio.Sort = columnaOrdenar ;
				dwSesionDirectorio.RowFilter= Utilitario.Helper.ObtenerFiltro();
				grid.DataSource = dwSesionDirectorio;

				grid.Columns[0].FooterText = Utilitario.Constantes.TEXTOFOOTERTOTAL;
				grid.Columns[Utilitario.Constantes.POSICIONTOTAL].FooterText = dwSesionDirectorio.Count.ToString();

				CImpresion oCImpresion = new CImpresion();
				oCImpresion.GuardarDataImprimirExportar(dtSesionDirectorio,Helper.ObtenerValorString(Enumerados.SeccionesArchivoConfiguracion.ConfiguracionesAplicativo.ToString(),Utilitario.Constantes.CODIGOTITULOREPORTESESIONES),columnaOrdenar,indicePagina);
				//ibtnImprimir.Visible = true;
			}
			else
			{
				grid.DataSource = dtSesionDirectorio;
				//ibtnImprimir.Visible = false;
			}
		
			try
			{
				grid.DataBind();
			}
			catch	
			{
				grid.CurrentPageIndex = 0;
				grid.DataBind();
			}
		}

